const AppSettingsContext = createContext(null);

export function AppSettingsProvider({ children }) {
  const [language, setLanguage] = useState(() => {
    const saved = localStorage.getItem('autocontrol-language');
    return saved === 'ru' || saved === 'kk' ? saved : 'ru';
  });
  const [theme, setTheme] = useState(() => {
    const saved = localStorage.getItem('autocontrol-theme');
    return saved === 'light' || saved === 'dark' ? saved : 'light';
  });

  useEffect(() => {
    localStorage.setItem('autocontrol-language', language);
  }, [language]);

  useEffect(() => {
    localStorage.setItem('autocontrol-theme', theme);
    document.documentElement.dataset.theme = theme;
  }, [theme]);

  const value = useMemo(() => ({
    language,
    setLanguage,
    theme,
    setTheme,
    t: (key, params = {}) => {
      const dictionary = translations[language] || translations.ru;
      const template = dictionary[key] || translations.ru[key] || key;
      return Object.entries(params).reduce((text, [name, value]) => text.replace(`{${name}}`, value), template);
    },
  }), [language, theme]);

  return <AppSettingsContext.Provider value={value}>{children}</AppSettingsContext.Provider>;
}

export function useAppSettings() {
  return useContext(AppSettingsContext);
}
